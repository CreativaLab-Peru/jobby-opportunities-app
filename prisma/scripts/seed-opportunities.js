function buildSearchVector(opportunity) {
  return [
    opportunity.title,
    opportunity.description,
    opportunity.fieldOfStudy || "",
    ...(opportunity.areas || []),
    ...(opportunity.requiredSkills || []),
    ...(opportunity.optionalSkills || []),
  ]
    .filter(Boolean)
    .join(" ")
    .toLowerCase();
}

async function seedSkills() {
  await Promise.all(
    skills.map(([name, key]) =>
      prisma.skill.upsert({
        where: { key },
        update: { name },
        create: { name, key },
      })
    )
  );
}

async function seedOrganizations() {
  await Promise.all(
    organizations.map(([name, key, logoUrl]) =>
      prisma.organization.upsert({
        where: { key },
        update: { name, logoUrl },
        create: { name, key, logoUrl },
      })
    )
  );
}

async function seedAreas() {
  await Promise.all(
    areasCatalog.map(([name, key]) =>
      prisma.area.upsert({
        where: { key },
        update: { name },
        create: { name, key },
      })
    )
  );
}

async function seedOpportunities() {
  let created = 0;
  let updated = 0;

  for (const opp of opportunities) {
    const existing = await prisma.opportunity.findFirst({
      where: { url: opp.url },
      select: { id: true },
    });

    const data = {
      ...opp,
      searchVector: buildSearchVector(opp),
      status: "ACTIVE",
    };

    if (existing?.id) {
      await prisma.opportunity.update({
        where: { id: existing.id },
        data,
      });
      updated += 1;
      continue;
    }

    await prisma.opportunity.create({ data });
    created += 1;
  }

  return { created, updated };
}

async function main() {
  console.log("🌱 Seeding organizations, skills, areas and opportunities...");

  await seedSkills();
  await seedOrganizations();
  await seedAreas();
  const result = await seedOpportunities();

  console.log("✅ Seed completado");
  console.log(`   - Skills: ${skills.length}`);
  console.log(`   - Organizations: ${organizations.length}`);
  console.log(`   - Areas: ${areasCatalog.length}`);
  console.log(`   - Opportunities created: ${result.created}`);
  console.log(`   - Opportunities updated: ${result.updated}`);
}

main()
  .catch((error) => {
    console.error("❌ Error seeding opportunities:", error);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
